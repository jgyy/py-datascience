"""
Arithmetic operators
"""


def wrapper():
    """
    label it as wrapper function to avoid renaming it
    """
    print(15 + 23)
    print(26 - 50)
    print(20 / 4)
    print(22 / 4)
    print(22 % 4)
    print(float(22) / 4)
    print(22.0 / 4)
    print(6 * 8)
    print(15 ** 2)


if __name__ == "__main__":
    wrapper()
